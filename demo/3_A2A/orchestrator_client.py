if __name__ == "__main__":
    client = OrchestratorRESTClient()
    
    if client.check_health():
        print("Orchestrator is online! Initiating task...\n")
        
        test_schema = """
        CREATE TABLE employees (
          id INTEGER PRIMARY KEY,
          name TEXT NOT NULL,
          department TEXT,
          salary NUMERIC NOT NULL
        );
        """
        
        test_task = "Write a simple Python main-style application that queries a sqlite3 database to return the employee who has the highest salary."
        
        print("Streaming response:\n")
        # Demonstrating the streaming capability 
        stream_generator = client.generate_completion(
            task=test_task,
            schema=test_schema,
            dialect="sqlite3",
            stream=True
        )
        
        for text_chunk in stream_generator:
            print(text_chunk, end="", flush=True)
            
        print("\n\nTask execution concluded.")
    else:
        print("Failed to connect to the orchestrator. Is the service running?")